// Hook to use magic bento effects
export const useMagicBento = () => {
  const isMobile = useMobileDetection();
  const gridRef = useRef<HTMLElement>(null);
  
  return {
    isMobile,
    gridRef,
    glowColor: DEFAULT_GLOW_COLOR,
  };
};
